    // создаем экземпл€р класса Random дл€ генерировани€ случайных чисел
    public static void Shuffle(int[] arr)
        { 
    Random rand = new Random();
 
    for (int i = arr.Length - 1; i >= 1; i--)
    {
        int j = rand.Next(i + 1);

        int tmp = arr[j];
        arr[j] = arr[i];
        arr[i] = tmp;
    }
}

